let y,x=7;
y=x++; //post increment 
console.log(y); // x is already assigned to y, then it got increment so y=7
console.log(x); // x got updated so x=8

let a,b=2;
a=++b //pre increment 
console.log(a)// b is already got incremented while before assigned to a, so b=3 
console.log(b) // a got incremented, so a=3


let j,k= 6;
j=k--;
console.log(j) // so j=6
console.log(k) // so k =5

let s,t= 6;
s=--t;
console.log(s) //so s=5
console.log(t) //so t=5
